namespace vc.Ifx.Options;

/// <summary>
/// Specifies whether to ignore case in comparisons.
/// </summary>
public enum IgnoreCaseOption
{

    /// <summary>
    /// Indicates that case should be ignored.
    /// </summary>
    Yes = 0,

    /// <summary>
    /// Indicates that case should not be ignored.
    /// </summary>
    No
}
